<center><h3>PENERIMAAN MAHASISWA BARU</h3></center>
<p style="text-align:justify">
    Satu kenyataan objektif, bahwa sebagian besar dari masyarakat Indonesia menuntut pelaksanaan pengembangan ilmu pengetahuan agama, ilmu pengetahuan dan teknologi 
    dan/atau seni yang dijiwai nilai-nilai keislaman. Tuntutan tersebut tidak bisa dihindarkan dalam rangka mencerdaskan kehidupan bangsa, maka kehadiran Universitas 
    Nahdlatul Ulama Indonesia yang mengembangkan ilmu-ilmu keislaman secara terpadu dengan ilmu-ilmu modern merupakan keniscayaan.
</p>
<br />

<center><h3>Visi</h3></center>
<p style="text-align:justify">
    Visi Universitas Nahdlatul Ulama Indonesia dirumuskan dengan: “Menjadi perguruan tinggi unggul berbasis keislaman, keilmuan dan kebangsaan”.
    <br><br>
    Spirit dan watak keislaman secara parsial tidak menjadi satu-satunya strategi pengembangan melainkan mengharuskan suatu pendekatan keilmuan yang di dalamnya 
    terjiwai watak  riset, nalar kritis, rasional dan kreatif.  Keunggulan yang terintegrasi di dalam keislaman dan keilmuan masih dirasa terlalu normatif dan 
    instrumental sehingga pengembangan Universitas Nahdlatul Ulama Indonesia ke depan menghajatkan etika komunikasi yang membumi ke dalam konteks kebangsaan. Singkat kata, visi pengembangan Universitas Nahdlatul Ulama Indonesia 
    ke depan diyakini akan mencapai sasaran apabila karakteristik "unggul" yang melekat pada sifat Universitas Nahdlatul Ulama Indonesia menjiwai trilogi keislaman, keilmuan, dan kebangsaan.
</p>

<center><h3>Misi</h3></center>
<p style="text-align:justify">
    <ol>
        <li>Menyelenggarakan pendidikan tinggi yang bermutu dan mampu menjawab tantangan jaman.</li>
        <li>Melakukan reintegrasi epistimologi keilmuan untuk menghilangkan dikotomi ilmu umum dan ilmu agama.</li>
        <li>Memberikan landasan moral terhadap pengembangan sain dan teknologi.</li>
        <li>Mengartikulasikan ajaran Islam secara kreatif, moderat dan toleran dalam konteks kehidupan bermasyarakat, berbangsa dan bernegara.</li>
        <li>Mempertahankan nilai-nilai lama yang masih relevan dan mengambil nilai-nilai baru yang lebih relevan.</li>
        <li>Mengembangkan penelitian dan kajian ilmiah.</li>
        <li>Memberikan kontribusi terhadap peningkatan kualitas hidup masyarakat melalui pola pengabdian masyarakat yang lebih profesional.</li>
        <li>Memberikan landasan moral dan spritual untuk mencapai pembangunan manusia seutuhnya.</li>
        <li>Memberikan kontribusi dalam mewujudkan perdamaian dunia dan kesejahteraan umat manusia.</li>
    </ol>
</p>

<center><h3>Tujuan</h3></center>
<p style="text-align:justify">
    Mempersiapkan lulusan untuk menjadi bagian dari masyarakat yang memiliki kemampuan akademik secara profesional yang dapat menerapkan, mengembangkan serta 
    menciptakan ilmu pengetahuan agama Islam serta mengupayakan pengalamannya untuk meningkatkan taraf kehidupan masyarakat dan memperkaya khasanah kebudayaan nasional.
</p>
