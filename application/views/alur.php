<p><h5>1. Buka Browser pada Komputer atau Smartphone Anda</h5></p>
<p><h5>2. Buka alamat website pmb.unusia.ac.id</h5></p>
<p><h5>3. Silahkan lakukan registrasi user terlebih dahulu pada menu yang telah disediakan (Persiapkan NIK Anda sebagai kata kunci untuk pendaftaran)</h5></p>
<p><h5>4. Silahkan login menggunakan akun yang telah didaftarkan</h5></p>
