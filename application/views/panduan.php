<h2 class="title">Fasilitas Penunjang Perkuliahan</h2>

<p>UNIVERSITAS NAHDLATUL ULAMA INDONESIA memiliki berbagai fasilitas yang menunjang keberhasilan pembelajaran di kampus antara lain:</p>

<p align="justify">
	<strong>a. Kampus</strong>
	<br>
	Kampus terdiri tiga gedung terpisah :<br>
	&nbsp;&nbsp;&nbsp;1. Kampus I memiliki 4 lantai<br>
	&nbsp;&nbsp;&nbsp;2. Kampus II memiliki 2 lantai<br>
	&nbsp;&nbsp;&nbsp;3. Kampus III memiliki 5 lantai
</p>

<p align="justify">
	<strong>b. Peralatan Ruang Kuliah</strong>
	<br>
	Untuk memperlancar proses belajar mengajar mahasiswa, terdapat 14 ruang kuliah yang cukup representatif dan setiap ruang kuliah dilengkapi dengan 
	whiteboard, AC, LCD, meja dan kursi dosen dan kursi mahasiswa, serta lampu penerang secukupnya.
</p>

<p align="justify">
	<strong>c. Peralatan Ruang Administrasi Akademik dan Tata Usaha</strong>
	<br>
	Dalam upaya memperlancar proses administrasi akademik dan tata usaha serta pelayanan mahasiswa, terdapat 3 (tiga) ruang Program Studi yang diperuntukkan bagi 
	Ketua Program Studi dan ruang administrasi akademik.<br>
	Ruang rapat atau ruang dosen. Ruang kantor dilengkapi dengan seperangkat peralatan kantor antara lain, 9 meja kerja, telepon dan faksimile dan 5 unit komputer. 
	Kesemuanya dipersiapkan dalam upaya kemudahan proses kerja dan pelayanan prima. Seluruh peralatan yang ada dalam kondisi baik, terawat dan milik sendiri.
</p>

<p align="justify">
	<strong>d. Perpustakaan</strong>
	<br>
	Perpustakaan tersedia di Setiap kampus.
</p>

<p align="justify">
	<strong>e. Laboratorium Bahasa</strong>
	<br>
	UNU Jakarta saat ini dilengkapi dengan peralatan laboratorium Bahasa yang memadai untuk proses belajar & mengajar terutama mata kuliah Bahasa.
</p>

<p align="justify">
	<strong>f. Laboratorium Komputer dan Pendukung Pembelajaran</strong>
	<br>
	Fasilitas Laboratorium komputer disediakan untuk mendukung pelayanan mahasiswa. Komputer yang dimiliki laboratorium komputer saat ini memiliki spesifikasi yang 
	cukup baik yaitu Pentium IV dengan program-program yang lengkap.
</p>

<p align="justify">
	<strong>g. Laboratorium Microteaching</strong>
	<br>
	Untuk meningkatkan kompetensi mahasiswa UNU Jakarta maka keberadaan Laboratorium microteaching menjadi sarana bagi mahasiswa yang sangat penting. Laboratorium 
	microteacing dilengkapi dengan spesifikasi yang lengkap ditunjang dengan teknologi serta software terkini.
</p>

<p align="justify">
